const os = require("os");

//Platform system OS name
console.log(os.platform());

//CPU architecture
console.log(os.arch());

//CPU core info
console.log(os.cpus());

//Free memory
console.log(os.freemem());

//Total Memory
console.log(os.totalmem());

//Home directory
console.log(os.homedir());

//Uptime
console.log(os.uptime());
